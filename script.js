let all = document.querySelector('#home')
let tv = document.querySelector('#tv')
let moves = document.querySelector('#moves')


let dram = document.querySelector('#light')
let dark = document.querySelector('#dark')
let news = document.querySelector('#new')

let showData = (movie)=>{
let move = document.querySelector(".movies")
move.innerHTML  = movie.map((movie)=>`
    <div class="main">
       <div style="padding:10px;"class = "hover_effect">
    <img src=${movie.poster_path} class="img"/>
         </div>
    </div>
`).join("")
}
showData(movie)

tv.addEventListener("click",()=>{
  let newmovie = movie.filter((movie)=>movie.category == 'Horror')
  showData(newmovie)
})
moves.addEventListener("click",()=>{
  let newmovie = movie.filter((movie)=>movie.category == 'Sci-Fi')
  showData(newmovie)
})
dram.addEventListener("click",()=>{
  let newmovie = movie.filter((movie)=>movie.category == 'Animation')
  showData(newmovie)
})
news.addEventListener("click",()=>{
  let newmovie = movie.filter((movie)=>movie.category == 'Action')
  showData(newmovie)
})
dark.addEventListener("click",()=>{
  let newmovie = movie.filter((movie)=>movie.category == 'Thriller')
  showData(newmovie)
})

all.addEventListener("click",()=>{
  showData(movie)
})

